function getDaysWord(n: number): string {
  const abs = Math.abs(n) % 100;
  const rem = abs % 10;
  if (abs >= 11 && abs <= 19) return 'дней';
  if (rem === 1) return 'день';
  if (rem >= 2 && rem <= 4) return 'дня';
  return 'дней';
}
